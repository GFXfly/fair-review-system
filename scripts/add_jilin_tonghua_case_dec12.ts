
import { PrismaClient } from '@prisma/client';

const prisma = new PrismaClient();

async function main() {
    // 1. 创建通报记录
    // 通告机构：国家市场监督管理局
    // 通告日期：2023-12-12
    const report = await prisma.report.create({
        data: {
            title: '国家市场监督管理局关于吉林省市场监管厅纠正通化市自然资源局滥用行政权力排除、限制竞争行为的通报',
            department: '国家市场监督管理局',
            publishDate: '2023-12-12',
            province: '吉林省'
        }
    });

    console.log(`创建吉林省通报记录，ID: ${report.id}`);

    const newCase = await prisma.case.create({
        data: {
            title: '吉林省市场监管厅纠正通化市自然资源局滥用行政权力排除、限制竞争行为',
            content: `2023年4月23日，吉林省市场监管厅依法对通化市自然资源局涉嫌滥用行政权力排除、限制竞争行为为立案调查。
    经查，2020年3月17日，当事人牵头印发《关于印发〈通化市房屋建筑和市政基础设施工程“多测合一”实施细则（试行）〉的通知》（通市自然资联发〔2020〕1号），规定从事“多测合一”的测绘中介服务机构应为拥有工程测量（控制测量、地形测量、规划测量、建筑工程测量、市政工程测量、线路与桥隧测量及地下管线测量）和不动产测绘（地籍测绘、房产测绘）专业的测绘资质单位，并建立通化市“多测合一”中介服务机构名录库，限定房屋建筑和市政基础设施工程项目业主在“多测合一”服务机构名录中选择测绘单位。
    吉林省市场监管厅认为，当事人制定印发文件设置不合理的“多测合一”名录库，限定建设单位仅可选择名录库内测绘单位开展相关测绘活动，排除、限制了测绘市场公平竞争。当事人的行为违反了《中华人民共和国反垄断法》第三十九条“行政机关和法律、法规授权的具有管理公共事务职能的组织不得滥用行政权力，限定或者变相限定单位或者个人经营、购买、使用其指定的经营者提供的商品”和第四十五条“行政机关和法律、法规授权的具有管理公共事务职能的组织不得滥用行政权力，制定含有排除、限制竞争内容的规定”的规定，构成滥用行政权力排除、限制竞争行为。
    调查期间，当事人积极整改，及时废止和修改相关文件，消除相关竞争限制，并在通化市自然资源局网站进行公布。当事人向吉林省市场监管厅提交了整改报告，表示将全面开展自查自纠，举一反三进一步加强公平竞争审查，防止排除、限制竞争的政策措施出台。`,
            violationType: '设置名录库/限定交易',
            violationClause: '《中华人民共和国反垄断法》第三十九条、第四十五条',
            documentName: '《关于印发〈通化市房屋建筑和市政基础设施工程“多测合一”实施细则（试行）〉的通知》（通市自然资联发〔2020〕1号）',
            documentOrg: '通化市自然资源局',
            province: '吉林省',
            violationDetail: '建立中介服务机构名录库，限定项目业主只能在名录库中选择测绘单位，排除了其他具有资质的机构。',
            result: '及时废止和修改相关文件，并在网站公布；向省市场监管厅提交整改报告。',
            publishDate: '2023-12-12',
            reportId: report.id
        }
    });

    console.log(`✓ 已添加案例: ${newCase.title}`);
}

main()
    .catch((e) => {
        console.error(e);
        process.exit(1);
    })
    .finally(async () => {
        await prisma.$disconnect();
    });
