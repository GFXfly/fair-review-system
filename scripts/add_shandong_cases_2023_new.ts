async function main() {
    console.log('开始添加山东省2023年违反公平竞争审查标准典型案例...');

    let count = 0;
    for (const data of cases) {
        // 检查是否已存在
        const existing = await prisma.case.findFirst({
            where: {
                title: data.title
            }
        });

        if (existing) {
            console.log(`跳过已存在的案例: ${data.title}`);
            continue;
        }

        await prisma.case.create({
            data: {
                title: data.title,
                content: data.content,
                province: data.province,
                publishDate: data.publishDate,
                violationType: data.violationType,
                result: data.result
            }
        });
        console.log(`已添加: ${data.title}`);
        count++;
    }

    console.log(`\n处理完成，共添加 ${count} 个新案例。`);
}

main()
    .catch((e) => {
        console.error(e);
        process.exit(1);
    })
    .finally(async () => {
        await prisma.$disconnect();
    });
