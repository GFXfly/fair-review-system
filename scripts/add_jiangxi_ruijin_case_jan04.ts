
import { PrismaClient } from '@prisma/client';

const prisma = new PrismaClient();

async function main() {
    // 1. 创建/查找江西省局通报 (Report)
    // 通告机构：江西省市场监督管理局
    // 通告日期：2024-01-04
    const report = await prisma.report.create({
        data: {
            title: '江西省市场监督管理局纠正瑞金市城市管理局滥用行政权力排除、限制竞争行为通报',
            department: '江西省市场监督管理局',
            publishDate: '2024-01-04',
            province: '江西省'
        }
    });

    console.log(`创建通报记录，ID: ${report.id}`);

    const newCase = await prisma.case.create({
        data: {
            title: '江西省瑞金市城市管理局滥用行政权力排除、限制竞争行为（2024-01-04通报版）',
            content: `2023年11月9日，江西省市场监督管理局依法对瑞金市城市管理局涉嫌滥用行政权力排除、限制竞争行为为进行立案调查。
    经查，2022年9月7日，当事人委托某招标代理有限公司通过江西省公共资源交易网发布瑞金市共享二轮电动单车运营服务项目“不见面”电子化公开招标公告。2022年10月8日，招标确定了2家共享二轮电动单车运营企业为瑞金市规划区域范围内含经开区及各旅游景区共享二轮电动单车运营服务项目企业。2022年11月29日，当事人分别与上述2家共享二轮电动单车运营企业签订了《瑞金市共享二轮电动单车运营服务合同》，运营期限5年。
    江西省市场监管局认为，当事人在没有法律、法规依据的情况下，通过公开招标的方式确定2家共享二轮电动单车运营企业为瑞金市规划区域范围内含经开区及各旅游景区共享二轮电动单车运营服务项目企业，排除、限制了其他具有合格资质和服务能力的市场主体进入瑞金市规划区域范围内含经开区及各旅游景区经营共享二轮电动单车服务市场，妨碍了该市场的公平竞争，违反了《中华人民共和国反垄断法》第三十九条：“行政机关和法律、法规授权的具有管理公共事务职能的组织不得滥用行政权力，限定或者变相限定单位或者个人经营、购买、使用其指定的经营者提供的商品”的规定，构成滥用行政权力排除、限制竞争行为。
    调查期间，当事人认识到上述行为违反了《中华人民共和国反垄断法》规定，积极整改，向江西省市场监管局提交了整改报告，公告废止有关合同，2023年11月17日，通过官方网站发布引入共享单车的通告，予以公告，全面放开瑞金市范围内共享二轮电动单车市场，举一反三，落实公平竞争审查制度，健全公平竞争审查机制，有效防止排除、限制竞争行为。`,
            violationType: '限定交易/排斥竞争',
            violationClause: '《中华人民共和国反垄断法》第三十九条“行政机关和法律、法规授权的具有管理公共事务职能的组织不得滥用行政权力，限定或者变相限定单位或者个人经营、购买、使用其指定的经营者提供的商品”',
            documentName: '《瑞金市共享二轮电动单车运营服务合同》及相关招标公告',
            documentOrg: '江西省瑞金市城市管理局',
            province: '江西省',
            violationDetail: '通过公开招标方式确定2家企业为运营服务项目企业，运营期限5年，排除了其他具有合格资质和服务能力的经营主体进入市场。',
            result: '公告废止有关合同，全面放开瑞金市范围内共享二轮电动单车市场。',
            publishDate: '2024-01-04',
            reportId: report.id
        }
    });

    console.log(`✓ 已添加案例: ${newCase.title}`);
}

main()
    .catch((e) => {
        console.error(e);
        process.exit(1);
    })
    .finally(async () => {
        await prisma.$disconnect();
    });
