async function main() {
    console.log(`Starting hybrid schema ingestion for QnAs 2025 (First Batch)...`);

    for (const item of QnA_DATA) {
        // Construct the atomic title: [总局答疑XX]
        const paddedId = item.id.toString().padStart(2, '0');
        const title = `[总局答疑${paddedId}] ${item.question}`;

        // Define metadata
        const department = '国家市场监督管理总局';
        const level = '业务口径';
        const category = 'QA_Fragment'; // Special category for hidden fragments

        // Check if exists
        const existing = await prisma.regulation.findFirst({
            where: {
                title: title
            }
        });

        if (existing) {
            console.log(`Updating existing fragment: ${title}`);
            // Update content and ensure category/level match
            await prisma.regulation.update({
                where: { id: existing.id },
                data: {
                    content: item.answer,
                    level: level,
                    category: category,
                    publishDate: '2025-03-21'
                }
            });
        } else {
            const reg = await prisma.regulation.create({
                data: {
                    title: title,
                    content: item.answer,
                    department: department,
                    level: level,
                    category: category,
                    publishDate: '2025-03-21',
                }
            });
            console.log(`Created fragment: ${title}`);
        }
    }

    console.log('Hybrid ingestion complete.');
}

main()
    .catch(e => {
        console.error(e);
        process.exit(1);
    })
    .finally(async () => {
        await prisma.$disconnect();
    });
