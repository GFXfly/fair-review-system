
import { PrismaClient } from '@prisma/client';

const prisma = new PrismaClient();

async function main() {
    // 创建通报
    const report = await prisma.report.create({
        data: {
            title: '国家市场监督管理总局关于江西省市场监管局纠正德安县城市管理局滥用行政权力排除、限制竞争行为的通报',
            department: '国家市场监督管理总局',
            publishDate: '2023-11-29',
            province: '江西省'
        }
    });

    console.log(`创建江西德安通报记录，ID: ${report.id}`);

    // 创建案例
    const content = `2023年6月20日，江西省市场监管局依法对德安县城市管理局涉嫌滥用行政权力排除、限制竞争行为为进行立案调查。
    经查，当事人以引入德安县共享电单车公共出行服务合作伙伴的名义，分别与3家共享电单车运营企业签订合作协议，并确定上述3家共享电单车运营企业为德安县辖区内共享电单车服务的运营主体。其中与甲共享电单车运营企业签订合作协议，限定投放数量1000辆，运营期限5年；与乙共享电单车运营企业签订合作协议，运营期限2年；与丙共享电单车运营企业签订合作协议，限定投放数量1000辆，运营期限2年。此后未允许其他共享电单车运营企业进入德安县共享电单车市场。
    江西省市场监管局认为，当事人没有法律、法规及国家政策依据，通过签订合作协议方式，确定3家共享电单车运营企业为德安县共享电单车公共出行服务合作伙伴的行为，限制了其他具有合格资质和服务能力的经营主体进入德安县主城区经营共享电单车服务市场，妨碍了该市场的公平竞争，违反了《中华人民共和国反垄断法》第四十条“行政机关和法律、法规授权的具有管理公共事务职能的组织不得滥用行政权力，通过与经营者签订合作协议、备忘录等方式，妨碍其他经营者进入相关市场或者对其他经营者实行不平等待遇，排除、限制竞争”的规定，构成滥用行政权力排除、限制竞争行为。
    调查期间，当事人认识到上述行为违反了《中华人民共和国反垄断法》规定，积极整改，向江西省市场监管局提交了整改报告，废止有关合同，通过官方网站发布引入共享电单车的通告，予以公告，全面放开德安县主城区共享电单车市场，将进一步落实公平竞争审查制度，健全公平竞争审查机制，有效防止排除、限制竞争行为。`;

    await prisma.case.create({
        data: {
            title: '江西省市场监管局纠正德安县城市管理局滥用行政权力排除、限制竞争行为',
            content: content,
            violationType: '签订排他性协议/限制市场准入',
            violationClause: '《中华人民共和国反垄断法》第四十条“行政机关和法律、法规授权的具有管理公共事务职能的组织不得滥用行政权力，通过与经营者签订合作协议、备忘录等方式，妨碍其他经营者进入相关市场或者对其他经营者实行不平等待遇，排除、限制竞争”',
            documentName: '共享电单车公共出行服务合作协议',
            documentOrg: '德安县城市管理局',
            province: '江西省',
            violationDetail: '通过签订合作协议，指定3家企业运营，限制其他企业进入市场。',
            result: '废止有关合同，全面放开市场，发布通告。',
            publishDate: '2023-11-29',
            reportId: report.id
        }
    });

    console.log('✓ 已添加案例: 江西省市场监管局纠正德安县城市管理局滥用行政权力排除、限制竞争行为');
}

main()
    .catch((e) => {
        console.error(e);
        process.exit(1);
    })
    .finally(async () => {
        await prisma.$disconnect();
    });
