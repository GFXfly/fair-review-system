三、审查程序
（一）政策措施审查
起草单位应当在政策措施起草阶段开展公平竞争审查，并作出是否符合公平竞争审查标准的书面审查结论。
拟由县级以上人民政府出台或提请本级人民代表大会及其常务委员会审议的政策措施，起草单位应当开展初审，并将政策措施草案和初审意见送市场监督管理部门审查。

（二）招标文件审查
招标文件由招标人在编制阶段进行公平竞争审查。招标人应当建立健全内部公平竞争审查机制，明确审查机构和程序。

