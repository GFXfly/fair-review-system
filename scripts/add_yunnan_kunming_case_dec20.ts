
import { PrismaClient } from '@prisma/client';

const prisma = new PrismaClient();

async function main() {
    // 1. 创建通报记录
    // 通告机构：国家市场监督管理局
    // 通告日期：2023-12-20
    const report = await prisma.report.create({
        data: {
            title: '国家市场监督管理局关于云南省市场监管局纠正昆明市五华区人民政府办公室滥用行政权力排除、限制竞争行为的通报',
            department: '国家市场监督管理局',
            publishDate: '2023-12-20',
            province: '云南省'
        }
    });

    console.log(`创建云南省通报记录，ID: ${report.id}`);

    const newCase = await prisma.case.create({
        data: {
            title: '云南省市场监管局纠正昆明市五华区人民政府办公室滥用行政权力排除、限制竞争行为',
            content: `2023年7月24日，云南省市场监管局依法对昆明市五华区人民政府办公室涉嫌滥用行政权力排除、限制竞争行为为立案调查。
    经查，2021年11月22日，当事人印发《昆明市五华区人民政府办公室关于印发五华区城市更新改造项目合作企业（伙伴）进入和退出实施办法的通知》（五政办通〔2021〕97号），要求城市更新改造项目合作企业（伙伴）中标人须在五华辖区范围内注册成立全资子公司或控股的合资项目公司。
    云南省市场监管局认为，当事人制定印发文件，要求城市更新改造项目合作企业（伙伴）中标人须在五华辖区范围内注册成立全资子公司或控股的合资项目公司，违背了市场公平竞争原则。违反了《中华人民共和国反垄断法》第四十五条“行政机关和法律、法规授权的具有管理公共事务职能的组织不得滥用行政权力，制定含有排除、限制竞争内容的规定”的规定，构成滥用行政权力排除、限制竞争行为。
    调查期间，当事人积极整改，主动纠正违法行为，发文废止了上述文件，并在政府信息公开上公示，消除了不良社会影响，恢复了市场公平竞争。`,
            violationType: '强制本地注册/设立分支机构',
            violationClause: '《中华人民共和国反垄断法》第四十五条“行政机关和法律、法规授权的具有管理公共事务职能的组织不得滥用行政权力，制定含有排除、限制竞争内容的规定”',
            documentName: '《昆明市五华区人民政府办公室关于印发五华区城市更新改造项目合作企业（伙伴）进入和退出实施办法的通知》（五政办通〔2021〕97号）',
            documentOrg: '昆明市五华区人民政府办公室',
            province: '云南省',
            violationDetail: '要求城市更新改造项目合作企业（伙伴）中标人须在五华辖区范围内注册成立全资子公司或控股的合资项目公司。',
            result: '发文废止了上述文件，并在政府信息公开上公示。',
            publishDate: '2023-12-20',
            reportId: report.id
        }
    });

    console.log(`✓ 已添加案例: ${newCase.title}`);
}

main()
    .catch((e) => {
        console.error(e);
        process.exit(1);
    })
    .finally(async () => {
        await prisma.$disconnect();
    });
