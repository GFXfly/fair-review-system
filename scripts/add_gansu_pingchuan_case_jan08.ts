
import { PrismaClient } from '@prisma/client';

const prisma = new PrismaClient();

async function main() {
    // 1. 查找甘肃省局通报 (Report ID 43)
    // 依然是“甘肃省市场监管局”，日期 2024-01-18 的那个通报（虽然图片里日期写的是 2024-01-08，稍等）
    // 仔细看图片，通告日期确实是：2024-01-08 (1月8日)
    // 之前的两张甘肃案例图片日期是 2024-01-18 (1月18日)
    // 这可能就是同一批通报，只是不同渠道发布时间有细微差异，或者是系统识别/打印错误，或者是分批发的。
    // 为了严谨，如果日期不同，理应建立新通报。但考虑到主题完全一致（都是“甘肃省市场监管局纠正...”），且内容和之前的“平川区”案例完全一致。

    // **重要发现**：这个“白银市平川区”的案例，我们在 Report 42 (国家局通报版) 里录入了。
    // 在 Report 43 (省局通报1月18日版) 里，我们录入了“白银市保健委”案。
    // 这张新图是“白银市平川区”案的省局通报版，日期是 1月8日。

    // 策略：创建一个新的省局通报记录 (1月8日)，录入这个案例。这样能保证数据源的忠实还原。
    const report = await prisma.report.create({
        data: {
            title: '甘肃省市场监管局纠正白银市平川区人民政府及平川区卫生健康局滥用行政权力排除、限制竞争行为通报',
            department: '甘肃省市场监管局',
            publishDate: '2024-01-08', // 图片显示的日期
            province: '甘肃省'
        }
    });

    console.log(`创建通报记录，ID: ${report.id}`);

    const newCase = await prisma.case.create({
        data: {
            title: '甘肃省白银市平川区人民政府及平川区卫生健康局滥用行政权力排除、限制竞争行为（2024-01-08通报版）',
            content: `2023年9月28日，甘肃省市场监管局依法对白银市平川区人民政府及白银市平川区卫生健康局涉嫌滥用行政权力排除、限制竞争行为为立案调查。
    经查，2023年2月22日，白银市平川区人民政府办公室印发《关于加强全区公共场所从业人员预防性健康体检工作的通知》，规定：“选定符合预防性健康体检机构设置要求的医疗机构承担全区预防性健康体检工作，逐步实现公共卫生服务与医疗服务分开。未经区卫生健康局指定，擅自开展体检活动的，由区卫生健康局依据相关规定进行处罚”。2023年3月1日，白银市平川区卫生健康局印发《关于确定全区食品和公共场所从业人员预防性健康体检定点医疗机构的通知》，其中规定：“经区卫健局局务会议研究，确定白银市中心医院为全区食品和公共场所从业人员的预防性健康体检定点医疗机构，承担全区预防性健康体检工作”。
    甘肃省市场监管局认为，预防性健康体检市场是一个竞争的市场，食品和公共场所从业人员可以自主选择具有合格资质和服务能力的体检机构进行预防性健康体检，具有合格资质和服务能力的健康体检机构均可以为食品和公共场所从业人员提供预防性健康体检服务。白银市平川区人民政府在无相关法律法规依据的情况下，出台政策措施对限定食品和公共场所从业人员预防性健康体检机构作出规定，白银市平川区卫生健康局通过印发通知直接指定预防性健康体检机构，排除了其他具有合格资质和服务能力的健康体检机构参与当地预防性健康体检市场的竞争，违反了《中华人民共和国反垄断法》第三十九条“行政机关和法律、法规授权的具有管理公共事务职能的组织不得滥用行政权力，限定或者变相限定单位或者个人经营、购买、使用其指定的经营者提供的商品”和第四十五条“行政机关和法律、法规授权的具有管理公共事务职能的组织不得滥用行政权力，制定含有排除、限制竞争内容的规定”的规定，构成滥用行政权力排除、限制竞争行为。
    调查期间，白银市平川区人民政府及白银市平川区卫生健康局充分认识到其相关行为违反了《中华人民共和国反垄断法》有关规定，主动改正违法行为，及时废止了相关文件并在政府网站发布公告，向甘肃省市场监管局提交了整改报告，积极消除相关后果，恢复预防性健康体检市场的竞争秩序。`,
            violationType: '限定交易/指定服务',
            violationClause: '《中华人民共和国反垄断法》第三十九条“行政机关和法律、法规授权的具有管理公共事务职能的组织不得滥用行政权力，限定或者变相限定单位或者个人经营、购买、使用其指定的经营者提供的商品”和第四十五条',
            documentName: '《关于加强全区公共场所从业人员预防性健康体检工作的通知》、《关于确定全区食品和公共场所从业人员预防性健康体检定点医疗机构的通知》',
            documentOrg: '甘肃省白银市平川区人民政府及平川区卫生健康局',
            province: '甘肃省',
            violationDetail: '印发通知选定并指定白银市中心医院为全区食品和公共场所从业人员的预防性健康体检定点医疗机构，未经指定擅自开展体检活动将受处罚。',
            result: '主动改正违法行为，及时废止了相关文件并在政府网站发布公告，向甘肃省市场监管局提交了整改报告。',
            publishDate: '2024-01-08',
            reportId: report.id
        }
    });

    console.log(`✓ 已添加案例: ${newCase.title}`);
}

main()
    .catch((e) => {
        console.error(e);
        process.exit(1);
    })
    .finally(async () => {
        await prisma.$disconnect();
    });
