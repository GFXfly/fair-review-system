
import { PrismaClient } from '@prisma/client';

const prisma = new PrismaClient();

async function main() {
    // ------------------------------------------------------------------
    // 案列 1：辽宁盘锦案例（2024-01-02 通报版）
    // ------------------------------------------------------------------
    // 通告日期：2024-01-02
    // 通告机构：辽宁省市场监督管理局
    // 该案例内容与之前国家局（Report 41）通报的内容（辽宁盘锦案）一致，但这是省局的原始通报。
    // 我们单独收录，保持数据源完整性。
    const reportLiaoning = await prisma.report.create({
        data: {
            title: '辽宁省市场监督管理局纠正盘锦市住房和城乡建设局滥用行政权力排除、限制竞争行为通报',
            department: '辽宁省市场监督管理局',
            publishDate: '2024-01-02',
            province: '辽宁省'
        }
    });
    console.log(`创建辽宁通报记录，ID: ${reportLiaoning.id}`);

    await prisma.case.create({
        data: {
            title: '辽宁省盘锦市住房和城乡建设局滥用行政权力排除、限制竞争行为（2024-01-02通报版）',
            content: `2023年9月6日，辽宁省市场监管局依法对盘锦市住房和城乡建设局涉嫌滥用行政权力排除、限制竞争行为为立案调查。
    经查，2022年11月8日，当事人印发《关于暂定兴隆台区液化石油气经营企业的批复》，明确“在某液化气公司新站建成使用前，由某液化气站负责经营兴隆台区区域液化石油气”。2023年3月9日，当事人印发《关于明确二界沟街道瓶装燃气经营主体的函》，明确“择优选择某液化石油气供应站从即日起作为二界沟区域燃气唯一合法经营主体，负责在二界沟街道（含元荣兴、荣滨区域）经营瓶装液化石油气”。截至调查前，前述文件均在执行。
    辽宁省市场监管局认为，当事人制发文件直接指定两家企业分别为兴隆台区和大洼区二界沟街道瓶装液化石油气唯一经营企业，导致其他企业无法在相应区域经营，排除、限制了相关市场竞争，违反了《中华人民共和国反垄断法》第三十九条“行政机关和法律、法规授权的具有管理公共事务职能的组织不得滥用行政权力，限定或者变相限定单位或者个人经营、购买、使用其指定的经营者提供的商品”和第四十五条“行政机关和法律、法规授权的具有管理公共事务职能的组织不得滥用行政权力，制定含有排除、限制竞争内容的规定”的规定，构成滥用行政权力排除、限制竞争行为。
    调查期间，当事人积极整改，及时废止原文件，向辽宁省市场监管局提交整改报告，全面开展自查自纠，举一反三落实公平竞争审查制度，防止排除、限制竞争的政策措施出台。`,
            violationType: '限定交易/指定独家经营',
            violationClause: '《中华人民共和国反垄断法》第三十九条“行政机关和法律、法规授权的具有管理公共事务职能的组织不得滥用行政权力，限定或者变相限定单位或者个人经营、购买、使用其指定的经营者提供的商品”和第四十五条',
            documentName: '《关于暂定兴隆台区液化石油气经营企业的批复》、《关于明确二界沟街道瓶装燃气经营主体的函》',
            documentOrg: '辽宁省盘锦市住房和城乡建设局',
            province: '辽宁省',
            violationDetail: '直接指定两家企业分别为兴隆台区和大洼区二界沟街道瓶装液化石油气唯一经营企业。',
            result: '及时废止原文件，向辽宁省市场监管局提交整改报告。',
            publishDate: '2024-01-02',
            reportId: reportLiaoning.id
        }
    });

    // ------------------------------------------------------------------
    // 案列 2：安徽凤阳案例（2024-01-02 通报版）
    // ------------------------------------------------------------------
    // 通告日期：2024-01-02
    // 通告机构：安徽省市场监督管理局
    // 同样，这是省局原始通报版。
    const reportAnhui = await prisma.report.create({
        data: {
            title: '安徽省市场监督管理局纠正滁州市凤阳县住房和城乡建设局滥用行政权力排除、限制竞争行为通报',
            department: '安徽省市场监督管理局',
            publishDate: '2024-01-02',
            province: '安徽省'
        }
    });
    console.log(`创建安徽通报记录，ID: ${reportAnhui.id}`);

    await prisma.case.create({
        data: {
            title: '安徽省滁州市凤阳县住房和城乡建设局滥用行政权力排除、限制竞争行为（2024-01-02通报版）',
            content: `2023年9月6日，安徽省市场监管局依法对凤阳县住房和城乡建设局涉嫌滥用行政权力排除、限制竞争行为为立案调查。
    经查，2021年3月，当事人委托某公司以公开招标方式征集并确定两家企业进入凤阳县共享单车、助力车市场运营。2021年5月，当事人分别与上述两家中标单位签订《凤阳县城区共享单车、助力车入场合同》，合同有效期为2年。2023年4月，原合同即将到期，当事人经局长办公会议研究决定再次以公开招标方式重新招标两家企业进入凤阳县共享单车、助力车市场运营。2023年5月，经公开招标，当事人确定两家企业为中标单位，运营期限为3年。
    安徽省市场监管局认为，当事人在没有法律、法规依据的情况下，通过公开招标方式确定两家企业进入凤阳县共享单车、助力车市场运营，排除了其他具有合格资质和服务能力的经营主体进入该县主城区经营共享单车、助力车服务，妨碍了该市场的公平竞争。当事人的上述行为违反了《中华人民共和国反垄断法》第三十九条“行政机关和法律、法规授权的具有管理公共事务职能的组织不得滥用行政权力，限定或者变相限定单位或者个人经营、购买、使用其指定的经营者提供的商品”的规定，构成滥用行政权力排除、限制竞争行为。
    在调查过程中，当事人主动整改，在该局官方网站发布《关于撤销凤阳县共享单车（电动车）相关文件的公告》，开放共享单车、助力车运营市场，恢复和维护公平竞争的市场环境。`,
            violationType: '限定交易/限制数量',
            violationClause: '《中华人民共和国反垄断法》第三十九条“行政机关和法律、法规授权的具有管理公共事务职能的组织不得滥用行政权力，限定或者变相限定单位或者个人经营、购买、使用其指定的经营者提供的商品”',
            documentName: '《凤阳县城区共享单车、助力车入场合同》及相关招标公告',
            documentOrg: '安徽省滁州市凤阳县住房和城乡建设局',
            province: '安徽省',
            violationDetail: '通过公开招标方式确定两家企业进入市场运营，排除了其他经营主体。',
            result: '在官方网站发布公告撤销相关文件，开放共享单车、助力车运营市场。',
            publishDate: '2024-01-02',
            reportId: reportAnhui.id
        }
    });

    console.log(`✓ 已添加辽宁盘锦、安徽凤阳案例（2024-01-02 通报版）`);
}

main()
    .catch((e) => {
        console.error(e);
        process.exit(1);
    })
    .finally(async () => {
        await prisma.$disconnect();
    });
