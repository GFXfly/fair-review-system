（三）审查对象
行政机关和法律、法规授权的具有管理公共事务职能的组织（以下统称政策制定机关）在制定市场准入、产业发展、招商引资、招标投标、政府采购、经营行为规范、资质标准等涉及市场主体经济活动的规章、规范性文件和其他政策措施时，应当进行公平竞争审查，评估对市场竞争的影响，防止排除、限制市场竞争。
